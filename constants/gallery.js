// Utility functions for gallery management
export const getGalleryStats = () => ({
  totalVideos: videos.length,
  totalShots: shots.length,
  totalPhotos: photos.length,
  maxPossibleStacks: Math.min(
    videos.length,
    shots.length,
    Math.floor(photos.length / 4)
  ),
})

export const getStackData = (index) => {
  if (
    index >= videos.length ||
    index >= shots.length ||
    index * 4 + 3 >= photos.length
  ) {
    return null
  }

  return {
    video: videos[index],
    shot: shots[index],
    photos: photos.slice(index * 4, index * 4 + 4),
    flip: index % 2 === 1,
  }
}
